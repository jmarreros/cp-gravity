    <h1>Código Postal Integración Gravity</h1>
    <p>
        Integración de código postal en Gravity Forms para llenar automáticamente campos de ciudad, municipio, provincia
        y comunidad autónoma.
    </p>
    <p>
        Se requiere las siguientes tablas en la BD:
    </p>

    <ul>
        <li>data_ccaa_provincias</li>
        <li>data_cp</li>
        <li>data_municipios</li>
    </ul>

    <p>
        Se requiere que existan los campos en el formulario de Gravity Forms con los siguientes nombres de clases:
    </p>

    <p>
        Ejemplo:
    </p>


    <ul>
        <li>grupo1 datos cp</li>
        <li>grupo1 datos cities</li>
        <li>grupo1 datos ciudad</li>
        <li>grupo1 datos municipio</li>
        <li>grupo1 datos provincia</li>
        <li>grupo1 datos comunidad</li>
    </ul>


